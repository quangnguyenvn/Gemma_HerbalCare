  <p class="eyebrow">Safety policy</p>
  <h1>Not a doctor. Not a diagnosis.</h1>
  <section>
    <h2>How safety comes first</h2>
    <p>
      Gemma HerbalCare performs triage before retrieval. If the request includes red
      flags such as chest pain, difficulty breathing, severe dehydration, blood in stool
      or vomit, stroke symptoms, pregnancy with bleeding, or infant fever, the system
      returns urgent-care guidance and does not recommend herbs.
    </p>
  </section>
  <section>
    <h2>Serious disease boundaries</h2>
    <p>
      The app never claims herbs cure cancer, HIV/AIDS, tuberculosis, malaria, stroke,
      heart attack, sepsis, severe infection, kidney failure, liver failure, pregnancy
      complications, or uncontrolled diabetes. It never advises stopping prescribed
      medicine or replacing emergency care.
    </p>
  </section>
  <section>
    <h2>Evidence levels</h2>
    <p>
      Every herb record displays one of four labels: traditional, limited, moderate, or
      strong. The demo deliberately uses a small curated dataset instead of scraping the
      internet.
    </p>
  </section>
  <section>
    <h2>Future data sources</h2>
    <p>
      GBIF, Plants of the World Online, World Flora Online, and local clinical or public
      health partners are future integration candidates. Any expansion should preserve
      source URLs, regional availability confidence, contraindications, and interactions.
    </p>
  </section>
